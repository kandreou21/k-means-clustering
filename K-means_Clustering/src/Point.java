public class Point {
    private float x1;
    private float x2;
    private int team;
    private int lastRUN;
	
    public float getX1() {
        return x1;
    }

    public void setX1(float x1) {
        this.x1 = x1;
    }

    public float getX2() {
        return x2;
    }

    public void setX2(float x2) {
        this.x2 = x2;
    }

    public int getTeam() {
        return team;
    }

    public void setTeam(int team) {
        this.team = team;
    }

    public int getLastRUN() {
        return lastRUN;
    }

    public void setLastRUN(int lastRUN) {
        this.lastRUN = lastRUN;
    }
}
